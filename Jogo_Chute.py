import random


valor_aleatorio = random.randint(1,10)

acertou = False
while acertou == False:
    chute = int(input('Digite um valor de 1 a 10'))
    if chute > valor_aleatorio:
        print('Chutou Alto')
    elif chute < valor_aleatorio:
        print('Chutou Baixo')
    elif chute == valor_aleatorio:
        acertou = True
        print('ParabÃ©ns, voce acertou!')
